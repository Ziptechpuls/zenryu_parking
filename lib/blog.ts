/* ブログ記事データ
   SEO向けのコラム記事。記事を追加する場合は BLOG_POSTS に要素を足すだけで
   一覧ページ・個別ページ・sitemap・JSON-LD がすべて自動生成されます。 */

export interface BlogSection {
  /** 見出し (h2)。空文字なら見出しなしの導入段落として扱う */
  heading?: string;
  /** 段落 (複数可)。HTML (strong/a など) を含められる */
  paragraphs?: string[];
  /** 箇条書き */
  list?: string[];
}

export interface BlogPost {
  /** URL スラッグ /blog/<slug> */
  slug: string;
  /** <title> 用 (SEO) */
  title: string;
  /** 記事見出し h1 */
  h1: string;
  /** meta description */
  description: string;
  /** 記事カードの抜粋 */
  excerpt: string;
  /** カテゴリ / アイブロウ表示 */
  category: string;
  /** 検索キーワード */
  keywords: string[];
  /** 公開日 YYYY-MM-DD */
  date: string;
  /** 更新日 (任意) */
  updated?: string;
  /** 想定読了時間 (分) */
  readMinutes: number;
  /** ヒーローの漢字マーク */
  mark: string;
  /** 本文セクション */
  sections: BlogSection[];
  /** 記事末尾の FAQ (任意・FAQ 構造化データにも使用) */
  faqs?: { q: string; a: string }[];
}

